package com.chubb.dgmk.caf.domain.resource;

public class ProducerResource {

	private String producerNumber;
	private String producerSubNumber;
	private String producerName;
	private String streetAddressLine1;
	private String mailingAddress;
	private String prodAndProdSubNumber;
	
	/**
	 * @return the branchName
	 */
	public String getProducerNumber() {
		return producerNumber;
	}

	/**
	 * @param branchName the branchName to set
	 */
	public void setProducerNumber(String producerNumber) {
		this.producerNumber = producerNumber;
	}

	/**
	 * @return the branchName
	 */
	public String getProducerSubNumber() {
		return producerSubNumber;
	}

	/**
	 * @param branchName the branchName to set
	 */
	public void setProducerSubNumber(String producerSubNumber) {
		this.producerSubNumber = producerSubNumber;
	}

	/**
	 * @return the branchName
	 */
	public String getProducerName() {
		return producerName;
	}

	/**
	 * @param branchName the branchName to set
	 */
	public void setProducerName(String producerName) {
		this.producerName = producerName;
	}

	/**
	 * @return the branchName
	 */
	public String getStreetAddressLine1() {
		return streetAddressLine1;
	}

	/**
	 * @param branchName the branchName to set
	 */
	public void setStreetAddressLine1(String streetAddressLine1) {
		this.streetAddressLine1 = streetAddressLine1;
	}

	/**
	 * @return the mailingAddress
	 */
	public String getMailingAddress() {
		return mailingAddress;
	}

	/**
	 * @param mailingAddress to set
	 */
	public void setMailingAddress(String mailingAddress) {
		this.mailingAddress = mailingAddress;
	}

	/**
	 * @return the prodAndProdSubNumber
	 */
	public String getProdAndProdSubNumber() {
		return prodAndProdSubNumber;
	}

	/**
	 * @param prodAndProdSubNumber the prodAndProdSubNumber to set
	 */
	public void setProdAndProdSubNumber(String prodAndProdSubNumber) {
		this.prodAndProdSubNumber = prodAndProdSubNumber;
	}
	
}
