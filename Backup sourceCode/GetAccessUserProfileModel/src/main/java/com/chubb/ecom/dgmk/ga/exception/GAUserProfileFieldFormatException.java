package com.chubb.ecom.dgmk.ga.exception;

public class GAUserProfileFieldFormatException extends Exception {

	private static final long serialVersionUID = 1L;

	/**
	 * Constructs an <code>GAUserProfileFieldFormatException</code> without a detail message.
	 */
	public GAUserProfileFieldFormatException() {
		super();
	}

	/**
	 * Constructs an <code>GAUserProfileFieldFormatException</code> with a detail message.
	 * 
	 * @param s
	 *            the detail message.
	 */
	public GAUserProfileFieldFormatException(String s) {
		super(s);
	}
}