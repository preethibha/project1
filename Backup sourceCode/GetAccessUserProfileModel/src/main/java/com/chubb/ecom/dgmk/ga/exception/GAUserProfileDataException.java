package com.chubb.ecom.dgmk.ga.exception;

public class GAUserProfileDataException extends Exception {

	private static final long serialVersionUID = 1L;

	/**
	 * Constructs an <code>GAUserProfileDataException</code> without a detail message.
	 */
	public GAUserProfileDataException() {
		super();
	}

	/**
	 * Constructs an <code>GAUserProfileDataException</code> with a detail message.
	 * 
	 * @param s
	 *            the detail message.
	 */
	public GAUserProfileDataException(String s) {
		super(s);
	}
}